    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            c = (Client) viewPrincipal.getUser();
            listOfFinancialGoals = new FinancialGoalDAO().searchFinancialGoalClient(c);
            c.setListOfFinancialGoal(listOfFinancialGoals);
            listOfFinancialGoalsFounded = new ArrayList<>(listOfFinancialGoals);
            jCheckBoxCompleted.setSelected(false);
            jCheckBoxCurrentGoals.setSelected(false);
            jCheckBoxAllGoals.setSelected(true);
            fillTable(listOfFinancialGoals);
        }catch (SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Erro: n√£o foi poss√≠vel carregar suas receitas!");
        }
    }//GEN-LAST:event_formWindowOpened

    private void jSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchButtonActionPerformed
        listOfFinancialGoalsFounded.clear();
        if(jCheckBoxAllGoals.isSelected() && jCheckBoxCurrentGoals.isSelected() || jCheckBoxAllGoals.isSelected() && jCheckBoxCompleted.isSelected()
                || jCheckBoxCompleted.isSelected() && jCheckBoxCurrentGoals.isSelected()){
            JOptionPane.showMessageDialog(null, "Erro: Selecione apenas um campo!");
            jCheckBoxAllGoals.setSelected(false);
            jCheckBoxCurrentGoals.setSelected(false);
            jCheckBoxCompleted.setSelected(false);
        }else{
            fillListFounded();
            fillTable(listOfFinancialGoalsFounded);
        }
    }//GEN-LAST:event_jSearchButtonActionPerformed

    private void jChangeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jChangeButtonActionPerformed
        int row = jTableGoals.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(null, "Para alterar uma meta financeira √© necess√°rio selecion√°-la!");
        }else{
            if(!listOfFinancialGoals.get(row).getCompleted() || !listOfFinancialGoalsFounded.get(row).getCompleted()){
                ViewRegisterFinancialGoal vrfg = new ViewRegisterFinancialGoal(null, true);
                vrfg.preparedEdit(listOfFinancialGoalsFounded.get(row));
                vrfg.setVisible(true);
                formWindowOpened(null);
            }else{
                JOptionPane.showMessageDialog(null,"Erro: N√£o √© poss√≠vel alterar uma meta cumprida!");
            }
        }
    }//GEN-LAST:event_jChangeButtonActionPerformed

    private void jDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteButtonActionPerformed
        int row = jTableGoals.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(null, "Para excluir uma meta financeira √© necess√°rio selecion√°-la!");
        }else{
            if(JOptionPane.showConfirmDialog(null, "Deseja apagar a meta financeira " + listOfFinancialGoalsFounded.get(row).getName() +
                    "?", "Apagar meta ?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try{
                    new FinancialGoalDAO().delete(listOfFinancialGoalsFounded.get(row));
                    c.getListOfFinancialGoal().remove(listOfFinancialGoalsFounded.get(row));
                    JOptionPane.showMessageDialog(null, "Meta financeira exclu√≠da com sucesso!");
                    formWindowOpened(null);
                }catch (SQLException | ClassNotFoundException ex){
                    JOptionPane.showMessageDialog(null, "Erro: N√£o foi poss√≠vel excluir esta meta financeira.");
                }
            }
        }
    }//GEN-LAST:event_jDeleteButtonActionPerformed

