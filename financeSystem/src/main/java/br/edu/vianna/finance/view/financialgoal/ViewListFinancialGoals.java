    private void fillTable(ArrayList<FinancialGoal> listOfFinancialGoals){
        DefaultTableModel dftm = (DefaultTableModel) jTableGoals.getModel();

        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }

        for(FinancialGoal fg : listOfFinancialGoals){
            addRow(fg, dftm);
        }
    }

    private static void addRow(FinancialGoal fg, DefaultTableModel dftm) {
        String [] row = {fg.getName(), fg.getDescription(), ValueFormat.displayValueFormat(fg.getValue()), ValueFormat.displayValueFormat(fg.getActualValue()),
                DateFormat.formatDateToString(fg.getInicialDate()), DateFormat.formatDateToString(fg.getFinalDate()),
                String.valueOf(fg.calculateRemainingInstallments())};
        dftm.addRow(row);
    }

    private void fillListFounded() {
        for(FinancialGoal fg : listOfFinancialGoals){
            if(jCheckBoxAllGoals.isSelected() || jCheckBoxCompleted.isSelected() && fg.getCompleted() || jCheckBoxCurrentGoals.isSelected() && !fg.getCompleted()){
                listOfFinancialGoalsFounded.add(fg);
            }
        }
    }

