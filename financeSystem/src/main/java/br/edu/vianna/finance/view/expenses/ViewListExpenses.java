    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        jMonthField.setText("");
        jYearField.setText("");
        jLabelTotalValue.setText("");
        try{
            c = (Client) viewPrincipal.getUser();
            listOfExpenses = new ExpenseDAO().searchClientExpenses(c);
            c.setListOfExpenses(listOfExpenses);
            fillTable(listOfExpenses);
            jLabelTotalValue.setText(ValueFormat.displayValueFormat(c.calculateTotalExpenses()));
        }catch (SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro: não foi possível carregar suas receitas!");
        }
    }//GEN-LAST:event_formWindowOpened

    private void jSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchButtonActionPerformed
        try{
            if(jMonthField.getText().isBlank() || jYearField.getText().isBlank()){
                JOptionPane.showMessageDialog(rootPane, "Erro: Os campos mês e ano devem ser preenchidos!");
            }else if(!checkFields()){
                JOptionPane.showMessageDialog(rootPane, "Erro: Os campos mês e ano só aceitam valores numéricos.\nExemplo: Mês: 01 Ano: 2000");
            }else{
                listOfExpenses = c.returnExpensesPerMonthAndYear(jMonthField.getText(), jYearField.getText());
                jLabelTotalValue.setText(ValueFormat.displayValueFormat(c.calculateTotalExpensesByMonthAndYear(jMonthField.getText(), jYearField.getText())));
                fillTable(listOfExpenses);
            }
        }catch (SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro: não foi possível carregar suas despesas!");
        }

    }//GEN-LAST:event_jSearchButtonActionPerformed

    private void jChangeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jChangeButtonActionPerformed
        int row = jTableExpenses.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(rootPane, "Para alterar uma despesa financeira é necessário selecioná-la!");
        }else{
            ViewRegisterExpense vre = new ViewRegisterExpense(null, true);
            vre.preparedEdit(listOfExpenses.get(row));
            vre.setVisible(true);
            formWindowOpened(null);
        }
    }//GEN-LAST:event_jChangeButtonActionPerformed

    private void jDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteButtonActionPerformed
        int row = jTableExpenses.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(rootPane, "Para excluir uma despesa financeira é necessário selecioná-la!!");
        }else{
            if(JOptionPane.showConfirmDialog(rootPane, "Deseja apagar a despesa: " + listOfExpenses.get(row).getDescription() + " ?", "Apagar despesa ?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try{
                    new ExpenseDAO().delete(listOfExpenses.get(row));
                    JOptionPane.showMessageDialog(rootPane, "Despesa excluída com sucesso!");
                    formWindowOpened(null);
                }catch (SQLException | ClassNotFoundException ex){
                    JOptionPane.showMessageDialog(rootPane, "Erro: Não foi possível excluir esta despesa.");
                }
            }
        }
    }//GEN-LAST:event_jDeleteButtonActionPerformed

