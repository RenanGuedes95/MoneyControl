    private void fillTable(ArrayList<Expense> listOfExpenses){
        DefaultTableModel dftm = (DefaultTableModel) jTableExpenses.getModel();

        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }

        for(Expense e : listOfExpenses){
            if(!jMonthField.getText().isBlank() && !jYearField.getText().isBlank()){
                if(e.getDate().getMonthValue() == Integer.parseInt(jMonthField.getText()) && e.getDate().getYear() == Integer.parseInt(jYearField.getText())){
                    addRow(e, dftm);
                }
            }else if(jMonthField.getText().isBlank() && jYearField.getText().isBlank()){
                addRow(e, dftm);
            }
        }
    }

    private static void addRow(Expense e, DefaultTableModel dftm) {
        String [] row = {String.valueOf(e.getId()), e.getDescription(), ValueFormat.displayValueFormat(e.getValue()), e.getType().toString(), DateFormat.formatDateToString(e.getDate())};
        dftm.addRow(row);
    }

    private Boolean checkFields(){
        return DateFormat.validateMonth(jMonthField.getText()) && DateFormat.validadeYear(jYearField.getText());
    }

