    private Boolean checkFields() {
        if(jCpfField.getText().isEmpty() || jNameField.getText().isEmpty() || jEmailField.getText().isEmpty() || jLoginField.getText().isEmpty()
        || jPasswordField.getText().isEmpty() || jProfessionField.getText().isEmpty() || jMaritalStatusField.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos!");
            return false;
        }else{
            return true;
        }
    }

    private Boolean checkCPF(){
        if(CpfFormat.validateCpf(jCpfField.getText())){
            return true;
        }else{
            JOptionPane.showMessageDialog(rootPane, "O campo CPF s√≥ aceita valores num√©ricos, pontos e tra√ßo.\nExemplo: 111.222.333-44");
            return false;
        }
    }

