    private void jSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveButtonActionPerformed
        try{
            if(checkFields() && checkCPF()){
                Client c = new Client((CpfFormat.cpfFormat(jCpfField.getText())), jNameField.getText(), jLoginField.getText(), jEmailField.getText(), jPasswordField.getText(),
                        jProfessionField.getText(), jMaritalStatusField.getText());
                if(client == null){
                    new ClientDAO().insert(c);
                    JOptionPane.showMessageDialog(rootPane,"Cliente cadastrado com sucesso!");
                }else{
                    new ClientDAO().alter(c);
                    JOptionPane.showMessageDialog(rootPane,"Cliente alterado com sucesso!");
                }
            }else{
                return;
            }
            setVisible(false);
        }catch (SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(rootPane,"Erro: N√£o foi poss√≠vel cadastrar/alterar os dados deste cliente.\nCPF j√° cadastrado!");
        }
    }//GEN-LAST:event_jSaveButtonActionPerformed

    public void preparedEdit(Client c) {
        client = c;
        jCpfField.setText(client.getCpf());
        jNameField.setText(client.getName());
        jLoginField.setText(client.getLogin());
        jEmailField.setText(client.getEmail());
        jPasswordField.setText("");
        jProfessionField.setText(client.getProfession());
        jMaritalStatusField.setText(client.getMaritalStatus());
    }

