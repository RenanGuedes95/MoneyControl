    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        jMonthField.setText("");
        jYearField.setText("");
        jLabelTotalValue.setText("");
        try{
            c = (Client) viewPrincipal.getUser();
            listOfIncomes = new IncomeDAO().searchClientIncomes(c);
            c.setListOfIncomes(listOfIncomes);
            fillTable(listOfIncomes);
            jLabelTotalValue.setText(ValueFormat.displayValueFormat(c.calculateTotalIncomes()));
        }catch (SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro: não foi possível carregar suas receitas!");
        }
    }//GEN-LAST:event_formWindowOpened

    private void jSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchButtonActionPerformed
            try{
                if(jMonthField.getText().isBlank() || jYearField.getText().isBlank()){
                    JOptionPane.showMessageDialog(rootPane, "Erro: Os campos mês e ano devem ser preenchidos!");
                }else if(!checkFields()){
                    JOptionPane.showMessageDialog(rootPane, "Erro: Os campos mês e ano só aceitam valores numéricos.\nExemplo: Mês: 01 Ano: 2000");
                }else{
                    listOfIncomes = c.returnIncomesPerMonthAndYear(jMonthField.getText(), jYearField.getText());
                    jLabelTotalValue.setText(ValueFormat.displayValueFormat(c.calculateTotalIncomesByMonthAndYear(jMonthField.getText(), jYearField.getText())));
                    fillTable(listOfIncomes);
                }
            }catch (SQLException | ClassNotFoundException ex){
                JOptionPane.showMessageDialog(rootPane, "Erro: não foi possível carregar suas receitas!");
            }


    }//GEN-LAST:event_jSearchButtonActionPerformed

    private void jChangeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jChangeButtonActionPerformed
        int row = jTableIncomes.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(rootPane, "Para alterar uma receita financeira é necessário selecioná-la!");
        }else{
            ViewRegisterIncome vri = new ViewRegisterIncome(null, true);
            vri.preparedEdit(listOfIncomes.get(row));
            vri.setVisible(true);
            formWindowOpened(null);
        }
    }//GEN-LAST:event_jChangeButtonActionPerformed

    private void jDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteButtonActionPerformed
        int row = jTableIncomes.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(rootPane, "Para excluir uma receita financeira é necessário selecioná-la!");
        }else{
            if(JOptionPane.showConfirmDialog(rootPane, "Deseja apagar a receita " + listOfIncomes.get(row).getDescription() + " ?", "Apagar receita ?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try{
                    new IncomeDAO().delete(listOfIncomes.get(row));
                    JOptionPane.showMessageDialog(rootPane, "Receita excluída com sucesso!");
                    formWindowOpened(null);
                }catch (SQLException | ClassNotFoundException ex){
                    JOptionPane.showMessageDialog(rootPane, "Erro: Não foi possível excluir esta receita.");
                }
            }
        }
    }//GEN-LAST:event_jDeleteButtonActionPerformed

