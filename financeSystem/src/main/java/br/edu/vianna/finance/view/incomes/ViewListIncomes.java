    private void fillTable(ArrayList<Income> listOfIncomes){
        DefaultTableModel dftm = (DefaultTableModel) jTableIncomes.getModel();

        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }

        for(Income i : listOfIncomes){
            if(!jMonthField.getText().isBlank() && !jYearField.getText().isBlank()){
                if(i.getDate().getMonthValue() == Integer.parseInt(jMonthField.getText()) && i.getDate().getYear() == Integer.parseInt(jYearField.getText())){
                    addRow(i, dftm);
                }
            }else if(jMonthField.getText().isBlank() && jYearField.getText().isBlank()){
                addRow(i, dftm);
            }
        }
    }

    private static void addRow(Income i, DefaultTableModel dftm) {
        String [] row = {String.valueOf(i.getId()), i.getDescription(), ValueFormat.displayValueFormat(i.getValue()), i.getType().toString(), DateFormat.formatDateToString(i.getDate())};
        dftm.addRow(row);
    }

    private Boolean checkFields(){
        return DateFormat.validateMonth(jMonthField.getText()) && DateFormat.validadeYear(jYearField.getText());
    }

