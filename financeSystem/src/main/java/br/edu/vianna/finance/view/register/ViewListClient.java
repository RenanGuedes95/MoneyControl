    private void fillTable(ArrayList<Client> listOfClients){
        DefaultTableModel dftm = (DefaultTableModel) jTableClients.getModel();

        while(dftm.getRowCount() > 0){
            dftm.removeRow(0);
        }

        for(Client c : listOfClients){
            String [] row = {c.getName(), c.getCpf(), c.getEmail(), c.getProfession(), c.getMaritalStatus(), DateFormat.formatDateToString(c.getDateOfRegister().toLocalDate())};
            dftm.addRow(row);
        }
    }

