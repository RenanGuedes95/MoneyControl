    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            listOfClients = new ClientDAO().searchAll();
            listOfClientsFounded = new ArrayList<>(listOfClients);
            fillTable(listOfClients);
        }catch(SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Erro: Clientes não encontrados no banco de dados!");
        }
    }//GEN-LAST:event_formWindowOpened

    private void jSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchButtonActionPerformed
        listOfClientsFounded.clear();
        for(Client c : listOfClients){
            if(c.getName().toUpperCase().trim().contains(jNameField.getText().toUpperCase().trim())){
                listOfClientsFounded.add(c);
            }
        }
        fillTable(listOfClientsFounded);
        jNameField.setText("");
    }//GEN-LAST:event_jSearchButtonActionPerformed

