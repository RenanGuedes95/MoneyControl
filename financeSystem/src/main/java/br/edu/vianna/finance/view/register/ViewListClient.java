    private void jChangeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jChangeButtonActionPerformed
        int row = jTableClients.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(null, "Para alterar os dados de um cliente é necessário selecioná-lo!");
        }else{
            ViewRegisterClient vrc = new ViewRegisterClient(null, true);
            vrc.preparedEdit(listOfClientsFounded.get(row));
            vrc.setVisible(true);
            formWindowOpened(null);
        }
    }//GEN-LAST:event_jChangeButtonActionPerformed

    private void jDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteButtonActionPerformed
        int row = jTableClients.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(null, "Para deletar um cliente você precisa selecionar a linha referente ao mesmo!");
        }else{
            if(JOptionPane.showConfirmDialog(null, "Deseja apagar o cliente: " + listOfClientsFounded.get(row).getName() +
                    "?", "Apagar cliente ?", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try{
                    new ClientDAO().delete(listOfClientsFounded.get(row));
                    listOfClients.remove(listOfClientsFounded.get(row));
                    JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso!");
                    formWindowOpened(null);;
                }catch (SQLException | ClassNotFoundException ex){
                    JOptionPane.showMessageDialog(null, "Erro: Não foi possível excluir este usuário.");
                }
            }
        }
    }//GEN-LAST:event_jDeleteButtonActionPerformed

