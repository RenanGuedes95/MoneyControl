    private void jSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchButtonActionPerformed
        if(checkFields()){
            jLabelTotalIncomes.setText(ValueFormat.displayValueFormat(c.calculateTotalIncomesByMonthAndYear(jMonthField.getText(), jYearField.getText())));
            jLabelTotalExpenses.setText(ValueFormat.displayValueFormat(c.calculateTotalExpensesByMonthAndYear(jMonthField.getText(), jYearField.getText())));
            jLabelFinalBalance.setText(ValueFormat.displayValueFormat(c.calculateFinalBalance(jMonthField.getText(), jYearField.getText())));
        }
    }//GEN-LAST:event_jSearchButtonActionPerformed

