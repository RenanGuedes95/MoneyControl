    private void clearFields() {
        jMonthField.setText("");
        jYearField.setText("");
        jLabelTotalIncomes.setText("");
        jLabelTotalExpenses.setText("");
        jLabelFinalBalance.setText("");
    }

    private Boolean checkFields(){
        return DateFormat.validateMonth(jMonthField.getText()) && DateFormat.validadeYear(jYearField.getText());
    }

