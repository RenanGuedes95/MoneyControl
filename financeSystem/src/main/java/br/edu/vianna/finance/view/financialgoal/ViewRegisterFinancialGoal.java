    public void preparedEdit(FinancialGoal fg) {
        financialGoal = fg;
        jNameField.setText(financialGoal.getName());
        jDescriptionField.setText(financialGoal.getDescription());
        jValueField.setText(ValueFormat.displayValue(financialGoal.getValue()));
        jMonthlyContribuitionField.setText(ValueFormat.displayValue(financialGoal.getMonthlyContribuition()));
        jInicialDateField.setText(DateFormat.formatDateToString(financialGoal.getInicialDate()));
    }

    private Boolean checkFields() {
        if(jValueField.getText().isEmpty() || jNameField.getText().isEmpty() || jMonthlyContribuitionField.getText().isEmpty() || jInicialDateField.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos!");
            return false;
        }else{
            return true;
        }
    }

    private Boolean verifiedDate() {
        if(DateFormat.validateDate(jInicialDateField.getText())){
            return true;
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro: Data inválida. \nDigite a data no formato: dd/mm/aaaa.");
            jInicialDateField.setText("");
            return false;
        }
    }

    private Boolean verifiedValue() {
        if(ValueFormat.validateValue(jValueField.getText()) && ValueFormat.validateValue(jMonthlyContribuitionField.getText())){
            return true;
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro: Valor inválido. \nDigite apenas números, pontos e vírgula.");
            jValueField.setText("");
            jMonthlyContribuitionField.setText("");
            return false;
        }
    }


