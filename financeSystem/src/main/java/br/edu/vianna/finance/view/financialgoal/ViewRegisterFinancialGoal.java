    private void jSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveButtonActionPerformed
        try {
            if (checkFields() && verifiedDate() && verifiedValue()) {
                FinancialGoal fg = new FinancialGoal(jNameField.getText(), jDescriptionField.getText(), Double.parseDouble(ValueFormat.formatValue(jValueField.getText())),
                        Double.parseDouble(ValueFormat.formatValue(jMonthlyContribuitionField.getText())), DateFormat.formatStringToDate(jInicialDateField.getText()),
                        (Client) viewPrincipal.getUser());
                if (financialGoal == null) {
                    new FinancialGoalDAO().insert(fg);
                    fg.getClient().addFinancialGoal(fg);
                    JOptionPane.showMessageDialog(rootPane, "Meta financeira cadastrada com sucesso!");
                } else {
                    fg.setId(financialGoal.getId());
                    new FinancialGoalDAO().alter(fg);
                    JOptionPane.showMessageDialog(rootPane, "Meta financeira alterada com sucesso!");
                }
            }else{
                return;
            }

            setVisible(false);
        }catch (SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(rootPane,"Erro: Não foi possível cadastrar/alterar os dados desta meta financeira.");
        }
    }//GEN-LAST:event_jSaveButtonActionPerformed

